public class FirstApp2 {                              // Четвертое задание
    public static boolean sum (int a, int b) {
        int sum = a + b;
        if (sum >= 10 && sum <= 20) {
            return true;
        } else return false;
    }


    public static void positiveOrNegative(int a) {    // Пятое задание
        if (a >= 0) {
            System.out.println("Положительное число");
        }
        if (a < 0) {
            System.out.println("Отрицательное число");
        }
    }
    }





